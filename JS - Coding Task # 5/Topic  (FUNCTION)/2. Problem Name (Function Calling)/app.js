// calling the funtion multiples time with
// different parameters.

function double(paisa) {
    let number = paisa * 2;

    return number;
}

console.log(double(2));

//2nd time
function double(rupees) {
    let number = rupees * 2;

    return number;
}

console.log(double(4));

// 3rd time
function double(amount) {
    let number = amount * 2;

    return number;
}

console.log(double(6));

